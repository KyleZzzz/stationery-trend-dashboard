const PLATFORMS = ['淘宝', '天猫', '京东', '抖音', '拼多多'];
const HEATS = ['爆', '热', '暖', '稳'];

function seededRandom(seed) {
  let s = seed;
  return function() {
    s = (s * 1664525 + 1013904223) & 0xffffffff;
    return (s >>> 0) / 0xffffffff;
  };
}

function pick(arr, rng) { return arr[Math.floor(rng() * arr.length)]; }
function randRange(min, max, rng) { return Math.round(min + rng() * (max - min)); }

function generateData() {
  const categories = Object.keys(CATEGORIES_CONFIG);
  const promoProducts = [];
  const seasonalTrends = [];
  const hotwords = [];
  const trendProductData = [];
  const emergingProducts = [];

  categories.forEach((cat, ci) => {
    const cfg = CATEGORIES_CONFIG[cat];
    const rng = seededRandom(ci * 1000 + 42);

    // 12 hot products
    for (let i = 0; i < 12; i++) {
      const brand = pick(cfg.brands, rng);
      const nameBase = cfg.names[i % cfg.names.length];
      const name = `${brand} ${nameBase}`;
      const platform = pick(PLATFORMS, rng);
      const price = +(cfg.price[0] + rng() * (cfg.price[1] - cfg.price[0])).toFixed(1);
      const sales = randRange(cfg.sales[0], cfg.sales[1], rng);
      promoProducts.push({ name, platform, price, sales, category: cat });
    }

    // 6 seasonal trends
    const trendNames = ['618爆款', '暑期热卖', '考试必备', '开学预热', '毕业季精选', '日常刚需'];
    for (let i = 0; i < 6; i++) {
      seasonalTrends.push({
        name: `${cat} ${trendNames[i]}`,
        category: cat,
        growth: randRange(20, 250, rng),
        heat: pick(HEATS, rng)
      });
    }

    // 6 hotwords
    const wordTemplates = ['推荐', '测评', '平替', '学生党', '性价比', '好物分享'];
    for (let i = 0; i < 6; i++) {
      hotwords.push({
        word: `${cat}${wordTemplates[i]}`,
        category: cat,
        platforms: {
          jd: randRange(500000, 8000000, rng),
          taobao: randRange(1000000, 12000000, rng),
          pdd: randRange(500000, 6000000, rng),
          douyin: randRange(3000000, 25000000, rng),
          xhs: randRange(1000000, 15000000, rng)
        }
      });
    }

    // 6 trend products with platform data
    for (let i = 0; i < 6; i++) {
      const brand = pick(cfg.brands, rng);
      const nameBase = cfg.names[(i + 3) % cfg.names.length];
      const basePrice = +(cfg.price[0] + rng() * (cfg.price[1] - cfg.price[0])).toFixed(1);
      trendProductData.push({
        name: `${brand} ${nameBase}`,
        category: cat,
        platforms: PLATFORMS.map(p => {
          const factor = p === '拼多多' ? 0.7 : p === '京东' ? 1.15 : p === '小红书' ? 1.2 : 1;
          const avgPrice = +(basePrice * factor).toFixed(1);
          const sales = randRange(cfg.sales[0] * 0.3, cfg.sales[1] * 0.5, rng);
          return { platform: p === '淘宝' ? '淘宝/天猫' : p, avgPrice, sales, revenue: Math.round(avgPrice * sales) };
        })
      });
    }

    // 6 emerging products
    const reasons = [
      '抖音短视频播放量暴增', '小红书种草笔记爆发', '618期间搜索量飙升',
      '直播间持续爆单', '社交平台口碑传播', '考试/暑假场景需求激增'
    ];
    for (let i = 0; i < 6; i++) {
      const growth7d = randRange(80, 400, rng);
      const currentSales = randRange(10000, 80000, rng);
      const trendData = [];
      let base = Math.round(currentSales / (1 + growth7d / 100));
      for (let d = 0; d < 13; d++) {
        base = Math.round(base * (1 + (growth7d / 100) / 13 + (rng() - 0.3) * 0.05));
        trendData.push(base);
      }
      trendData[12] = currentSales;
      emergingProducts.push({
        name: `${pick(cfg.brands, rng)} ${cfg.names[(i + 6) % cfg.names.length]}`,
        category: cat,
        reason: reasons[i % reasons.length],
        currentSales,
        growth7d,
        predictScore: randRange(65, 98, rng),
        trendData
      });
    }
  });

  // Sort products by sales desc, assign ranks
  promoProducts.sort((a, b) => b.sales - a.sales);
  promoProducts.forEach((p, i) => { p.rank = i + 1; });

  return { promoProducts, seasonalTrends, hotwords, trendProductData, emergingProducts };
}

